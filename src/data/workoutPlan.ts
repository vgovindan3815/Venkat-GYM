function getDayIndex(dateKey: string): number {
  const date = new Date(`${dateKey}T00:00:00`)
  return Number.isNaN(date.getTime()) ? new Date().getDay() : date.getDay()
}

export function getWorkoutPlanForDate(dateKey: string): WorkoutDayPlan {
  const dayIndex = getDayIndex(dateKey)
  return WEEKLY_WORKOUT_PLAN[dayIndex === 0 ? 6 : dayIndex - 1]
}
